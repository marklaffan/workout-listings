var mongoose = require("mongoose");

// Schema Setup
var workoutSchema = new mongoose.Schema({
    workout: String,
    image: String,
    description: String,
    author: {
        id: {
            type: mongoose.Schema.Types.ObjectId,
            ref: "User"
        },
        username: String
    },
    comments: [
        {
            type: mongoose.Schema.Types.ObjectId,
            ref: "Comment"
        }
    ]
});

module.exports = mongoose.model("Workout", workoutSchema); 